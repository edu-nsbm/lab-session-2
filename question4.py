def main() -> None:
    length: float = 10.0
    width: float = 5.0
    area: float = width * length

    print(
        "The area of a rectangle with length "
        + str(length)
        + " and width "
        + str(width)
        + " is "
        + str(area)
    )


if __name__ == "__main__":
    main()
